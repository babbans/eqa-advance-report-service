package com.eqa.eqaaprsummaryservice.exception;

public class FileShareException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public FileShareException() {
		super();
	}

	public FileShareException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public FileShareException(String message, Throwable cause) {
		super(message, cause);
	}

	public FileShareException(String message) {
		super(message);
	}

	public FileShareException(Throwable cause) {
		super(cause);
	}
	
	

}
